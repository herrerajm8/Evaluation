     public ArrayList<Student> getStudentList(String query){
        ArrayList<Student> studList = new ArrayList<Student>();
        Connection con = getConnection();
        String que =query;
        Statement st;
        ResultSet rs;
        
        try{
            st = con.createStatement();
            rs = st.executeQuery(query);
            Student stu;
            while(rs.next()){
                stu = new Student(rs.getInt("idNumber"),rs.getString("fullName"),rs.getString("course"),rs.getString("birthdate"),rs.getString("gender"));
                studList.add(stu);
            }
        }catch(Exception e){
         JOptionPane.showMessageDialog(null, "getStudent?");
         e.printStackTrace();
        }
        return studList;
    }
    public void Show_Unenrolled_Student_table(){
        String query ="Select idNumber, fullName,course, birthdate,gender from `students` LEFT JOIN classlist ON idNumber = classlist.studentId WHERE classlist.studentId IS null";
        ArrayList<Student> list =  getStudentList(query);
        DefaultTableModel model = (DefaultTableModel)jTable2.getModel();
        
        Object[] row = new Object[5];
        for(int i = 0; i < list.size();i++){
            row[0] = list.get(i).getIdNumber();
            row[1] = list.get(i).getFullName();
            model.addRow(row);
        }
    }
    public void Show_Enrolled_Student_table(){
        String query = "Select * from `students` JOIN classlist ON idNumber = classlist.studentId";
        ArrayList<Student> list =  getStudentList(query);
        DefaultTableModel model = (DefaultTableModel)jTable1.getModel();
        
        Object[] row = new Object[5];
        for(int i = 0; i < list.size();i++){
            row[0] = list.get(i).getIdNumber();
            row[1] = list.get(i).getFullName();
            row[2] = list.get(i).getCourse();
            model.addRow(row);
        }
    }
     public void execute_Query(String query, String message){
        Connection con = getConnection();
        Statement st;
        try {
            st=con.createStatement();
            if((st.executeUpdate(query))==1){
                JOptionPane.showMessageDialog(null, "Student "+message+" successfully");
            }else{
                JOptionPane.showMessageDialog(null, "Data not "+message);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
    
