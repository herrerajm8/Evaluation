    public ArrayList<Faculty> getFacultyList(){
        ArrayList<Faculty> facList = new ArrayList<Faculty>();
        Connection con = getConnection();
        String query = "SELECT * FROM `faculty`";
        Statement st;
        ResultSet rs;
        
        try{
            st = con.createStatement();
            rs = st.executeQuery(query);
            Faculty fac;
            while(rs.next()){
                fac = new Faculty(rs.getInt("faculty_Id"),rs.getString("facultyName"),rs.getString("field"),rs.getString("gender"),rs.getString("contactInfo"));
//                fac = new Faculty(rs.getInt("faculty_Id"),rs.getString("fullName"),rs.getString("course"),rs.getString("birthdate"),rs.getString("gender"));
                facList.add(fac);
            }
        }catch(Exception e){
         JOptionPane.showMessageDialog(null, "getFaculty?");
         e.printStackTrace();
        }
        return facList;
    }
    
   
    public void execute_Query(String query, String message){
        Connection con = getConnection();
        Statement st;
        try {
            st=con.createStatement();
            if((st.executeUpdate(query))==1){
                JOptionPane.showMessageDialog(null, "Data "+message+" successfully");
            }else{
                JOptionPane.showMessageDialog(null, "Data not "+message);
            }
        }catch(Exception e){
            e.printStackTrace();
        }
    }
    
   


